// Use UZ as the structural source so TS keeps function signatures.
type TDict = typeof dicts.uz;

// Proxy that resolves keys against the active language at access time.
function makeProxy<T extends object>(path: string[]): T {
  return new Proxy({} as T, {
    get(_target, prop: string) {
      const segments = [...path, prop];
      let cur: any = (dicts as any)[currentLang] ?? dicts.uz;
      for (const s of segments) {
        if (cur == null) break;
        cur = cur[s];
      }
      // Fallback to UZ if missing in current lang.
      if (cur === undefined) {
        cur = dicts.uz as any;
        for (const s of segments) {
          if (cur == null) break;
          cur = cur[s];
        }
      }
      if (cur && typeof cur === "object" && !Array.isArray(cur) && typeof cur !== "function") {
        return makeProxy([...segments]);
      }
      return cur;
    },
  });
}

export const t: TDict = makeProxy<TDict>([]);
