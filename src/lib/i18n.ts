// Multi-language UI strings (UZ / RU / EN). Single source of truth.
// `t` is a Proxy that reads `currentLang` set at app boot from localStorage.
// Switching language calls `window.location.reload()` so every component
// re-imports the correct dictionary without prop drilling.

export type Lang = "uz" | "ru" | "en";

function readInitialLang(): Lang {
  if (typeof window === "undefined") return "uz";
  try {
    const saved = window.localStorage.getItem("lang");
    if (saved === "uz" || saved === "ru" || saved === "en") return saved;
  } catch {}
  return "uz";
}

export let currentLang: Lang = readInitialLang();

export function setActiveLang(l: Lang) {
  try {
    window.localStorage.setItem("lang", l);
  } catch {}
  currentLang = l;
  if (typeof window !== "undefined") window.location.reload();
}

